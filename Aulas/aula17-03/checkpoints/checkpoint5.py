# Faça uma função pode_votar(idade) que retorna True se idade >= 16.
# Critério de verificação: para 15, deve retornar False.

idade = int(input("Insira sua idade:"))

def pode_votar(idade):
    if idade >= 16:
        return True
    

if pode_votar == True:
    print("PODE VOTAR!")
else:
    print("NÃO PODE VOTAR!")